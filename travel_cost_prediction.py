import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
sns.set_style('darkgrid')

data = pd.read_csv('travel_cost_dataset_large.csv')

data

data.describe()

data.isnull().sum()

data.Transport.unique()

data['Transport'] = data['Transport'].map({'Bus':1,'Train':2,'Flight':3})

data

X = data.drop('Travel_Cost',axis=1)

X

y = data.Travel_Cost

y

from sklearn.model_selection import train_test_split

X_train,X_test,y_train,y_test = train_test_split(X,y,test_size=0.2,random_state=0)

X_train.shape

X_test.shape

from sklearn.linear_model import LinearRegression

model = LinearRegression()

model.fit(X_train,y_train)

y_pred = model.predict(X_test)

y_pred

y_test

from sklearn.metrics import r2_score

print("R2 Score:", r2_score(y_test, y_pred))

def predict_cost(distance, days, people, transport, hotel):
    return model.predict([[distance, days, people, transport, hotel]])

predict_cost(500, 3, 2, 2, 3)

predict_cost(10000,5,1,2,1)

